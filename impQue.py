my_list = [0, 1, 2, 3, 4, 5]

# Skip elements
print(my_list[::2])  # Output: [0, 2, 4]

# Reverse the sequence
print(my_list[::-1])  # Output: [5, 4, 3, 2, 1, 0]


# Slice using negative indices
print(my_list[-4:-1])  # Output: [2, 3, 4]


